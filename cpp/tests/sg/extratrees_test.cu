// Equal-mean property: variance-style regressor objectives give gain=0 when
// left and right share the parent's label_sum/count ratio. Tolerance is
// dtype-relative to absorb float32 residual from intermediate ops.
template <typename ObjT, typename BinT>
static void equal_mean_regressor_zero_gain()
{
  using DataT = typename ObjT::DataT;
  ObjT obj{1, 1};
  BinT left[1];
  BinT parent[1];
  if constexpr (std::is_same_v<BinT, AggregateBin>) {
    left[0]   = BinT{6.0, 60};
    parent[0] = BinT{10.0, 100};
  } else {
    left[0]   = BinT{6.0, 60.0, 60};
    parent[0] = BinT{10.0, 100.0, 100};
  }
  DataT gain    = obj.GainFromSideStats(left, parent);
  DataT eps_tol = DataT(100) * std::numeric_limits<DataT>::epsilon();
  EXPECT_NEAR(gain, DataT(0.0), eps_tol) << "Equal-mean split should give zero gain; got " << gain;
  // Also assert the helper did not hit a sentinel-return branch.
  EXPECT_GT(gain, -std::numeric_limits<DataT>::max() / DataT(2));
}

// Equal-class-distribution property: classifier split where left and right
// share the parent's class-frequency ratio gives gain=0.
template <typename ObjT, typename BinT>
static void equal_mean_classifier_zero_gain()
{
  using DataT = typename ObjT::DataT;
  ObjT obj{2, 1};
  BinT left[2];
  BinT parent[2];
  if constexpr (std::is_same_v<BinT, CountBin>) {
    left[0]   = BinT{30};
    left[1]   = BinT{20};
    parent[0] = BinT{60};
    parent[1] = BinT{40};
  } else {
    left[0]   = BinT{30.0, 30};
    left[1]   = BinT{20.0, 20};
    parent[0] = BinT{60.0, 60};
    parent[1] = BinT{40.0, 40};
  }
  DataT gain    = obj.GainFromSideStats(left, parent);
  DataT eps_tol = DataT(100) * std::numeric_limits<DataT>::epsilon();
  EXPECT_NEAR(gain, DataT(0.0), eps_tol)
    << "Equal-class-distribution split should give zero gain; got " << gain;
  EXPECT_GT(gain, -std::numeric_limits<DataT>::max() / DataT(2));
}

// Cover all 24 (objective, dtype) instantiations via a known property.
TEST(ExtraTreesGain, EqualMeanRegressorsZeroGain)
{
  equal_mean_regressor_zero_gain<MSEObjectiveFunction<float, float, int>, AggregateBin>();
