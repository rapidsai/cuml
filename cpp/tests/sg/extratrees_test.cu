  int n_cols                 = 2;
  int n_classes              = 2;

  thrust::device_vector<DataT> X  = X_host;
  thrust::device_vector<LabelT> y = y_host;

  RF_params rf_params = set_rf_params(/*max_depth*/ 3,
                                      /*max_leaves*/ -1,
                                      /*max_features*/ 1.0f,
                                      /*max_n_bins*/ 8,
                                      /*min_samples_leaf*/ 1,
