// 10000 draws across 15 categories, chi-square at p=0.001 (df=14, exact
// critical 36.1233; we use 36.12 for a slightly more conservative threshold).
// Guards against modulo bias, wrong polynomial, or off-by-one.
TEST(ExtraTreesRng, MonteCarloUniformity)
{
  constexpr int N            = 10000;
  constexpr int max_n_bins   = 16;
  constexpr int n_categories = max_n_bins - 1;
  constexpr int n_features   = 4;
  constexpr double kChi2Crit = 36.12;

  std::vector<int> hist(n_categories, 0);
  for (int trial = 0; trial < N; ++trial) {
    uint64_t seed   = static_cast<uint64_t>(trial + 1);
    uint64_t treeid = static_cast<uint64_t>(trial * 7) % 1024u;
    uint64_t nodeid = static_cast<uint64_t>(trial * 31) % 4096u;
    uint64_t col    = static_cast<uint64_t>(trial % n_features);
    int idx         = et_split_position<int>(seed, treeid, nodeid, col, max_n_bins);
    ASSERT_GE(idx, 0);
    ASSERT_LT(idx, n_categories);
    hist[idx]++;
  }

  double mu   = static_cast<double>(N) / static_cast<double>(n_categories);
  double chi2 = 0.0;
  for (int b = 0; b < n_categories; ++b) {
    double diff = hist[b] - mu;
    chi2 += (diff * diff) / mu;
  }
  EXPECT_LT(chi2, kChi2Crit) << "chi-square = " << chi2;
}

// Spearman rank correlation with average-rank tie handling. var_a and var_b
// are non-zero for N=10000 draws over 15 categories.
static double spearman_rank_corr(const std::vector<int>& a, const std::vector<int>& b)
{
  const size_t n   = a.size();
  auto rank_vector = [n](const std::vector<int>& v) {
    std::vector<size_t> indices(n);
    std::iota(indices.begin(), indices.end(), size_t(0));
    std::sort(indices.begin(), indices.end(), [&v](size_t i, size_t j) { return v[i] < v[j]; });
    std::vector<double> ranks(n);
    size_t i = 0;
    while (i < n) {
      size_t j = i;
      while (j + 1 < n && v[indices[j + 1]] == v[indices[i]]) {
        ++j;
      }
      double avg = (static_cast<double>(i) + static_cast<double>(j)) / 2.0 + 1.0;
      for (size_t k = i; k <= j; ++k) {
        ranks[indices[k]] = avg;
      }
      i = j + 1;
    }
    return ranks;
  };
  auto ra      = rank_vector(a);
  auto rb      = rank_vector(b);
  double mean  = (static_cast<double>(n) + 1.0) / 2.0;
  double cov   = 0.0;
  double var_a = 0.0;
  double var_b = 0.0;
  for (size_t i = 0; i < n; ++i) {
    double da = ra[i] - mean;
    double db = rb[i] - mean;
    cov += da * db;
    var_a += da * da;
    var_b += db * db;
  }
  return cov / std::sqrt(var_a * var_b);
}

// At N=10000, |rho| under the null has stderr ~0.01. Bound |rho| < 0.05
// (~5 stderr from zero). Guards against the failure mode where col gets
// XOR'd into the subsequence rather than fnv1a32-chained.
TEST(ExtraTreesRng, SpearmanPairCorrelation)
{
  constexpr int N                                    = 10000;
  constexpr int max_n_bins                           = 16;
  constexpr double kRhoBound                         = 0.05;
  const std::array<std::pair<int, int>, 3> col_pairs = {{{0, 1}, {1, 2}, {2, 3}}};

  for (auto cp : col_pairs) {
    std::vector<int> a(N), b(N);
    for (int trial = 0; trial < N; ++trial) {
      uint64_t seed   = static_cast<uint64_t>(trial + 1);
      uint64_t treeid = static_cast<uint64_t>(trial * 7) % 1024u;
      uint64_t nodeid = static_cast<uint64_t>(trial * 31) % 4096u;
      a[trial] =
        et_split_position<int>(seed, treeid, nodeid, static_cast<uint64_t>(cp.first), max_n_bins);
      b[trial] =
        et_split_position<int>(seed, treeid, nodeid, static_cast<uint64_t>(cp.second), max_n_bins);
    }
    double rho = spearman_rank_corr(a, b);
    EXPECT_LT(std::abs(rho), kRhoBound)
      << "cols=(" << cp.first << "," << cp.second << ") rho=" << rho;
  }
}

// Oracle 4 covers the 24 (objective, dtype) instantiations: 5 hand-derived
// gains for representative BinT shapes plus property-based zero-gain tests
// (equal-mean for regressors, equal-class-distribution for classifiers).

TEST(ExtraTreesGain, CountBinGiniHandComputed)
{
  using DataT = float;
  using IdxT  = int;
  using ObjT  = GiniObjectiveFunction<DataT, int, IdxT>;
  using BinT  = typename ObjT::BinT;

  ObjT obj{2, 1};
  BinT left[2]   = {BinT{30}, BinT{20}};
  BinT parent[2] = {BinT{70}, BinT{30}};
  DataT gain     = obj.GainFromSideStats(left, parent);

  // Proxy Gini gain (matches GainPerSplit; nLeft=nRight=50, len=100):
  //   j=0 (30/70): (30^2 + 70^2)/(50*100) - 100^2/100^2 = 0.50 - 0.49 = 0.01
  //   j=1 (20/30): (20^2 + 30^2)/(50*100) - 50^2/100^2  = 0.26 - 0.25 = 0.01
  EXPECT_NEAR(gain, DataT(0.02), DataT(1e-5));
}

TEST(ExtraTreesGain, CountBinEntropyHandComputed)
{
  using DataT = double;
  using IdxT  = int;
  using ObjT  = EntropyObjectiveFunction<DataT, int, IdxT>;
  using BinT  = typename ObjT::BinT;

  ObjT obj{2, 1};
  BinT left[2]   = {BinT{30}, BinT{20}};
  BinT parent[2] = {BinT{70}, BinT{30}};
  DataT gain     = obj.GainFromSideStats(left, parent);

  // Proxy entropy gain. Each (lval, rval, val) triple contributes
  //   log2(lval/nLeft)*lval/len + log2(rval/nRight)*rval/len
  //   - log2(val/len)*val/len.
  // j=0 (30, 40, 70): log2(0.6)*0.3 + log2(0.8)*0.4 - log2(0.7)*0.7
  //                = -0.2210897 - 0.1287712 + 0.3602012 = 0.0103403.
  // j=1 (20, 10, 30): log2(0.4)*0.2 + log2(0.2)*0.1 - log2(0.3)*0.3
  //                = -0.2643856 - 0.2321928 + 0.5210897 = 0.0245113.
  // Total = 0.0348516.
  EXPECT_NEAR(gain, DataT(0.0348516), DataT(1e-6));
}

TEST(ExtraTreesGain, AggregateBinMSEHandComputed)
{
  using DataT = double;
  using IdxT  = int;
  using ObjT  = MSEObjectiveFunction<DataT, DataT, IdxT>;
  using BinT  = typename ObjT::BinT;

  ObjT obj{1, 1};
  BinT left[1]   = {BinT{10.0, 60}};
  BinT parent[1] = {BinT{20.0, 100}};
  DataT gain     = obj.GainFromSideStats(left, parent);

  // parent_obj = -400/100 = -4. left_obj = -100/60 = -1.6667.
  // right_lbl = 20 - 10 = 10. right_obj = -100/40 = -2.5.
  // raw = -4 - (-1.6667 + -2.5) = 0.1667. gain = 0.1667 * 0.5/100 = 8.333e-4.
  EXPECT_NEAR(gain, DataT(8.3333333333e-4), DataT(1e-10));
}

